#include "parsing.h"

void	search_symbols(char const *s, size_t *i, size_t *count)
{
	if (s[(*i)] == '<')
	{
		(*count) += 2;
		(*i)++;
		if (s[(*i)] == '<')
			(*i)++;
	}
	else if (s[(*i)] == '>')
	{
		(*count) += 2;
		(*i)++;
		if (s[(*i)] == '>')
			(*i)++;
	}
	else if (s[(*i)] == '|')
	{
		(*count) += 2;
		(*i)++;
	}
	else
		(*i)++;
}
